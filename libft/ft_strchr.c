char	*ft_strchr(const char *s, int c)
{
	int x;

	x = 0;
	while (s[x])
	{
		if (s[x] == c)
			return ((char *)&s[x]);
		x++;
	}
	if (s[x] == c)
		return ((char *)&s[x]);
	return (0);
}
