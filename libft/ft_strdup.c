#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*s2;
	int		i;

	i = 0;
	size = ft_strlen(s1);
	s2 = malloc(sizeof(char) * (size + 1));
	if (s2 == NULL)
		return (0);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
