#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int	x;

	x = 0;
	while (s != NULL && s[x] != '\0')
	{
		write(fd, &s[x], 1);
		x++;
	}
	write(fd, "\n", 1);
}
